// build.ts generates src/tui/banner.ts and src/version.ts, then compiles
// binaries and stages npm packages. Exports the shared per-app contract:
// TARGETS and build({ kind, targets }).
//
// Standalone usage:
//   bun build.ts                                codegen only
//   bun build.ts --compile                      host binary → dist/covcom
//   bun build.ts --compile --targets all        every release target
//   bun build.ts --kind npm [--targets ...]     binary build, then npm staging

import { statSync } from 'node:fs';
import { rm } from 'node:fs/promises';
import { join } from 'node:path';
import { bundleVersion } from '../scripts/version.ts';
import { hostSuffix, pickTargets, stageNpm, type Target } from '../scripts/stage.ts';

const DIR = import.meta.dir;

export const TARGETS: Target[] = [
	{ suffix: 'darwin-arm64', bun: 'bun-darwin-arm64', os: 'darwin', cpu: 'arm64', bin: 'covcom-macos-arm64' },
	{ suffix: 'darwin-x64', bun: 'bun-darwin-x64', os: 'darwin', cpu: 'x64', bin: 'covcom-macos-x64' },
	{ suffix: 'linux-x64', bun: 'bun-linux-x64', os: 'linux', cpu: 'x64', libc: 'glibc', bin: 'covcom-linux-x64' },
	{ suffix: 'win32-x64', bun: 'bun-windows-x64', os: 'win32', cpu: 'x64', bin: 'covcom-win-x64.exe' },
];

async function bundleBanner(src: string, out: string): Promise<void> {
	process.stdout.write('Bundling banner...\n');
	const text = await Bun.file(src).text();
	await Bun.write(out,
		`// AUTO-GENERATED by build.ts. Do not edit.\nexport const BANNER = ${JSON.stringify(text)};\n`,
	);
	process.stdout.write(`  → ${out}\n`);
}

export async function codegen(): Promise<void> {
	await bundleBanner(join(DIR, 'src/ascii'), join(DIR, 'src/tui/banner.ts'));
	await bundleVersion(join(DIR, 'src/version.ts'));
}

// target null compiles for the host with today's unsuffixed name.
async function compile(target: Target | null): Promise<void> {
	const outfile = target
		? join(DIR, 'dist', target.bin)
		: join(DIR, 'dist', process.platform === 'win32' ? 'covcom.exe' : 'covcom');
	process.stdout.write(`Building ${target ? target.suffix : 'host'} binary...\n`);
	const result = await Bun.build({
		entrypoints: [join(DIR, 'src/main.ts')],
		compile: target ? { target: target.bun, outfile } : { outfile },
	});
	if (!result.success) {
		for (const msg of result.logs) process.stderr.write(`  ${msg.message}\n`);
		throw new Error(`${target ? target.suffix : 'host'} binary build failed`);
	}
	const size = statSync(outfile).size;
	process.stdout.write(`Built: ${outfile}  (${(size / 1024).toFixed(1)} KB)\n`);
}

export async function build(opts: { kind: 'binary' | 'npm' | 'spa'; targets?: string[] }): Promise<void> {
	if (opts.kind === 'spa') throw new Error('cli has no spa build');
	await codegen();
	if (opts.kind === 'binary') {
		if (!opts.targets) {
			await compile(null);
			return;
		}
		const picked = pickTargets(TARGETS, opts.targets);
		// A full-target build clears dist/ first so bake output is clean by
		// construction; single-target and host builds overwrite their own
		// outfile in place, leaving just-baked release binaries intact.
		if (picked.length === TARGETS.length) await rm(join(DIR, 'dist'), { recursive: true, force: true });
		for (const t of picked) await compile(t);
		return;
	}
	await stageNpm({
		metaName: 'covcom',
		scope: 'cli',
		binName: 'covcom',
		summary: 'Terminal client for COVCOM: post-quantum end-to-end encrypted ephemeral group chat.',
		directory: 'cli',
		distDir: join(DIR, 'dist'),
		targets: TARGETS,
		suffixes: opts.targets ?? [hostSuffix(TARGETS)],
	});
}

if (import.meta.main) {
	const args = process.argv.slice(2);
	const kindIdx = args.indexOf('--kind');
	const kind = kindIdx >= 0 ? args[kindIdx + 1] : undefined;
	const tIdx = args.indexOf('--targets');
	const targets = tIdx >= 0 ? args[tIdx + 1].split(',') : undefined;
	try {
		if (kind === 'npm') {
			const suffixes = targets ?? [hostSuffix(TARGETS)];
			await build({ kind: 'binary', targets: suffixes });
			await build({ kind: 'npm', targets: suffixes });
		} else if (args.includes('--compile') || targets) {
			await build({ kind: 'binary', targets });
		} else {
			await codegen();
		}
	} catch (e) {
		process.stderr.write(`${e instanceof Error ? e.message : String(e)}\n`);
		process.exit(1);
	}
}
